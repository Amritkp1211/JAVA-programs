/*Write a program in Java to display the first 10 natural numbers 
using while loop.*/
public class Assign10naturalno {
	public static void main(String[] args) {
		int x=1;
		System.out.println("firts ten natural no is ");
		while(x<11) {
			
			System.out.println(":"+(x++));
			
		}
		
	}

}
