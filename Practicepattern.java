package javaP;

public class Practicepattern {
	public static void main(String[] args) {

       for(int i=9;i>=0;i--) {
			
			for(int k=0;k<9-i;k++) {
				System.out.print(" ");
			}
		for(int j=0;j<=i;j++) {
			System.out.print(" *"); 
		}
		
		System.out.println();
		}
		
		for(int i=0;i<=9;i++) {
			
			for(int k=0;k<9-i;k++) {
				System.out.print(" ");
			}
		for(int j=0;j<=i;j++) {
			System.out.print(" *"); 
		}
		
		System.out.println();
		}

	}
	}


